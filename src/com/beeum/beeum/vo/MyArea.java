package com.beeum.beeum.vo;

public class MyArea {
	private int noArea;
	private int noCity;
	private int noUser;
	
	
	
	
	public MyArea() {
		super();
	}
	public int getNoArea() {
		return noArea;
	}
	public void setNoArea(int noArea) {
		this.noArea = noArea;
	}
	public int getNoCity() {
		return noCity;
	}
	public void setNoCity(int noCity) {
		this.noCity = noCity;
	}
	public int getNoUser() {
		return noUser;
	}
	public void setNoUser(int noUser) {
		this.noUser = noUser;
	}
	
	
}
