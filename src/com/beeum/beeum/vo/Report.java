package com.beeum.beeum.vo;

public class Report {
	private int noReport;
	private int noUser;
	private int noTarget;
	private String content;
	
	
	
	public Report() {
	}
	public int getNoReport() {
		return noReport;
	}
	public void setNoReport(int noReport) {
		this.noReport = noReport;
	}
	public int getNoUser() {
		return noUser;
	}
	public void setNoUser(int noUser) {
		this.noUser = noUser;
	}
	public int getNoTarget() {
		return noTarget;
	}
	public void setNoTarget(int noTarget) {
		this.noTarget = noTarget;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
}
