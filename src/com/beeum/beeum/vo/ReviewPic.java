package com.beeum.beeum.vo;

public class ReviewPic {
	private int noRevPic;
	private int noTlr;
	private int noTtr;
	private int noReservation;
	private String fileName;
	
	
	public ReviewPic() {
	}
	public int getNoRevPic() {
		return noRevPic;
	}
	public void setNoRevPic(int noRevPic) {
		this.noRevPic = noRevPic;
	}
	public int getNoTlr() {
		return noTlr;
	}
	public void setNoTlr(int noTlr) {
		this.noTlr = noTlr;
	}
	public int getNoTtr() {
		return noTtr;
	}
	public void setNoTtr(int noTtr) {
		this.noTtr = noTtr;
	}
	public int getNoReservation() {
		return noReservation;
	}
	public void setNoReservation(int noReservation) {
		this.noReservation = noReservation;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	
}
