package com.beeum.beeum.vo;

public class TourArea {
	private int noTourArea;
	private int noTour;
	private int noCity;
	
	
	public TourArea() {
	}
	public int getNoTourArea() {
		return noTourArea;
	}
	public void setNoTourArea(int noTourArea) {
		this.noTourArea = noTourArea;
	}
	public int getNoTour() {
		return noTour;
	}
	public void setNoTour(int noTour) {
		this.noTour = noTour;
	}
	public int getNoCity() {
		return noCity;
	}
	public void setNoCity(int noCity) {
		this.noCity = noCity;
	}
	
	
}
