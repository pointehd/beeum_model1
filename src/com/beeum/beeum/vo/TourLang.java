package com.beeum.beeum.vo;

public class TourLang {
	private int noTourLang;
	private int noTour;
	private int noLang;
	
	
	public TourLang() {
	}
	public int getNoTourLang() {
		return noTourLang;
	}
	public void setNoTourLang(int noTourLang) {
		this.noTourLang = noTourLang;
	}
	public int getNoTour() {
		return noTour;
	}
	public void setNoTour(int noTour) {
		this.noTour = noTour;
	}
	public int getNoLang() {
		return noLang;
	}
	public void setNoLang(int noLang) {
		this.noLang = noLang;
	}
	
}
